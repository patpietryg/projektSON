import math

def dodawanie(a, b):
    return a + b + 3

def odejmowanie(a, b):
    return a - b

def mnozenie(a, b):
    return a * b

def dzielenie(a, b):
    if b != 0:
        return a / b
    else:
        return "Błąd: Nie można dzielić przez zero"

def potegowanie(a, b):
    return a ** b

def pierwiastkowanie(a):
    return a ** 0.5

def logarytm(a):
    return math.log(a)


